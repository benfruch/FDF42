#include "libft.h"

char	*ft_reverse(char *s)
{
	int	len;
	int	i;

	i = 0;
	len = ft_strlen(s);
	while (i < len / 2)
	{
		ft_swap(&s[i], &s[len - i - 1]);
		i++;
	}
	return (s);
}
