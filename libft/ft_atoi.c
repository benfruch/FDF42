#include "libft.h"

int		ft_atoi(const char *str)
{
	int a;
	int n;
	int i;

	a = 1;
	n = 0;
	i = 0;
	while (str[i] == '\n' || str[i] == ' ' || str[i] == '\r'
			|| str[i] == '\t' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-')
		a = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + str[i] - '0';
		i++;
	}
	return (n * a);
}
