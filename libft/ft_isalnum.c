#include "libft.h"

int		ft_isalnum(int c)
{
	if (ft_isdigit(c) == 1 ||
			(c >= 'A' && c <= 'Z') ||
			(c >= 'a' && c <= 'z'))
		return (1);
	else
		return (0);
}
